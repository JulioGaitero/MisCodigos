public class T4For {
    public static void main(String[] args){

        int valores[] = new int[5];

        for(int i=0; i < valores.length; i++){

            valores[i] = i;
            System.out.println("Posicion numero: "+ i);

        }

        for(int i=0; i <= 10; i++){
            System.out.println(i);
        }
    }
}
