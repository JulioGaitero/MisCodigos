public class L3VariablesTipos {

    public static void main(String[] args) {

        // Numeros Enteros

        byte numero1 = 1; // 1 byte
        short numero2 = 2; // 2 byte
        int numero3 = 3; // 4 byte
        long numero4 = 4; // 8 byte

        // Numeros Decimales

        float decimal1 = 4.2f;
        double decimal2 = 4.7;

        // caracter

        char caracter1 = 'a';

        // boleanos

        boolean verdadero = true;
        boolean falso = false;

        // Cadenas de texto

        String nombre = "Nerea";
        String apellidos =  "Arzua";

        // Tipos envoltorio

        Integer numero5 = null;
        Long numero = 2L;

    }

}
