package Condicionales;

public class L1IfElse {
    public static void main(String[] args) {
        int edad = 18;
        boolean esMayor = edad >= 14;

        if (esMayor) {
            System.out.println("Es mayor de edad");
        } else {
            System.out.println("Es menor de edad");
        }

    }
}
