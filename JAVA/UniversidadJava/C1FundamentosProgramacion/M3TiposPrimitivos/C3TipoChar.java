package C1FundamentosProgramacion.M3TiposPrimitivos;

public class C3TipoChar {

    public static void main(String[] args) {

        char caracter = 'a';
        System.out.println("El caracter es: " +caracter);

        char  caracter2 = '\u0021';
        System.out.println("El caracter es utilizando unicode: " +caracter2);

        char caracter3 = 33;
        System.out.println("El caracter es utilizando el valor decimal unicode: " +caracter3);

        char caracter4 = '!';
        System.out.println("El caracter es utilizando el caracter: " +caracter4);

        int conocerDecimal = '!';
        System.out.println("El valor decimal unicode de ! es: " +conocerDecimal);

    }

}
