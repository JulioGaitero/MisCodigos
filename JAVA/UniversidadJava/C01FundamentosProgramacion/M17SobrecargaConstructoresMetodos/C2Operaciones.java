package C1FundamentosProgramacion.M17SobrecargaConstructoresMetodos;

public class C2Operaciones {

    public static int sumar(int a, int b) {
        return a + b;
    }

    public static int sumar(int a, int b, int c){
        return a + b + c;
    }

    public static int sumar(int a, int b, int c, int d){
        return a + b + c + d;
    }

    public static int sumar(int a, int b, int c, int d, int e){
        return a + b + c + d + e;
    }

    public static double sumar(double a, double b){
        return a + b;
    }

    public static double sumar(double a, double b, double c){
        return a + b + c;
    }

    public static double sumar(double a, double b, double c, double d){
        return a + b + c + d;
    }

}
