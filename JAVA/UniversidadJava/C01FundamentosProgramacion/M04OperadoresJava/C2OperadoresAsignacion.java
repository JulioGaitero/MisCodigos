package C1FundamentosProgramacion.M04OperadoresJava;

public class C2OperadoresAsignacion {

    public static void main(String[] args) {

        int a = 3;
        int b = 5;
        int c = a + 5 - b;

        System.out.println(c);

        a += 5;
        System.out.println(a);

        a -= 5;
        System.out.println(a);

        a *= 5;
        System.out.println(a);

        a /= 5;
        System.out.println(a);

        a %= 5;
        System.out.println(a);


    }

}
