package C1FundamentosProgramacion.M02VariablesJAVA;

public class C5Concatenacion {

    public static void main(String[] args) {
        String cadenaUno = "Hola";
        String cadenaDos = "Amigo";
        String cadenaCompeta = cadenaUno + " " + cadenaDos;

        System.out.println(cadenaCompeta);

        int variableNumerica = 3;
        int variableNumericaDos = 5;

        System.out.println("El valor de la suma de variables es : " +variableNumerica + " + " +variableNumericaDos + " = " + (variableNumericaDos + variableNumerica));
    }

}
