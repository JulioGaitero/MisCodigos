package C1FundamentosProgramacion.M03TiposPrimitivos;

public class C2NumericosFlotantes {

    public static void main(String[] args) {

        float numeroFloat = 10.0F;
        System.out.println("El valor minimo de un float es: " + Float.MIN_VALUE);
        System.out.println("El valor maximos de un float es: " + Float.MAX_VALUE);

        System.out.println(" ");
        double numeroDouble = 10.0;
        System.out.println("El valor minimo de un double es: " + Double.MIN_VALUE);
        System.out.println("El valor maximos de un double es: " + Double.MAX_VALUE);

    }
}
